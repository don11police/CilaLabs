package basicJavaProgramme;

public class ReverseNumber {
	
	public static void main(String[] args) {
		
		for(int i=1; i<=10; i++) {         //print 1 to 10 number
		
				
				System.out.println(i);
			
			
		}
		
		for(int i=10; i>=1; --i) {       //print 1 to 10 reverse number
			
			System.out.println( i);
		}
	}
	

}
